    private void btnAddProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProdukActionPerformed
        // TODO add your handling code here:
        DefaultTableModel list = (DefaultTableModel) tabelProduk.getModel();
        ArrayList<String> produk = new ArrayList<>();

        if (txtProduk.getText().equals("")||txtHarga.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Harap input nama produk dan harga!");
        } else {
            produk.add(txtProduk.getText());
            produk.add(txtHarga.getText());
            list.addRow(produk.toArray());
        }
    }//GEN-LAST:event_btnAddProdukActionPerformed

    private void txtHargaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtHargaKeyPressed
        // TODO add your handling code here:
        String value = txtHarga.getText();
        int l = value.length();
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            txtHarga.setEditable(true);
        } else {
            txtHarga.setEditable(false);
            JOptionPane.showMessageDialog(this, "Harap input harga dengan angka!");
        }
    }//GEN-LAST:event_txtHargaKeyPressed

