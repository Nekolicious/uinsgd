    private void pesanProduk() {
        DefaultTableModel produk = (DefaultTableModel) tabelProduk.getModel();
        DefaultTableModel keranjang = (DefaultTableModel) tabelKeranjang.getModel();
        int row = tabelProduk.getSelectedRow();
        ArrayList<String> data = new ArrayList<>();
        String qty = "";
        String input;

        String nmproduk = produk.getValueAt(row, 0).toString();
        String hrg = produk.getValueAt(row, 1).toString();
        do {
            input = JOptionPane.showInputDialog("Jumlah yang akan dibeli");
            if (input.matches("^[0-9]*$")) {
                qty = input;
            } else {
                JOptionPane.showMessageDialog(this, "Harap hanya input angka!");
            }
        } while (!input.matches("^[0-9]*$"));

        int harga = Integer.parseInt(hrg) * Integer.parseInt(qty);
        hrg = Integer.toString(harga);

        data.add(nmproduk);
        data.add(qty);
        data.add(hrg);

        keranjang.addRow(data.toArray());
        textTotalHarga.setText(totalHarga());
    }
    
    private void hapusKeranjang() {
        int opsi = JOptionPane.showConfirmDialog(null, "Ingin mengosongkan keranjang?");
        switch (opsi) {
            case JOptionPane.YES_OPTION:
                DefaultTableModel model = (DefaultTableModel) tabelKeranjang.getModel();
                model.setRowCount(0);
                break;
            case JOptionPane.NO_OPTION:
                break;
            default:
                break;
        }
        textTotalHarga.setText(totalHarga());
    }
    
    private void hapusItemKeranjang() {
        DefaultTableModel model = (DefaultTableModel) tabelKeranjang.getModel();
        int getSelectedRowForDeletion = tabelKeranjang.getSelectedRow();

        if (getSelectedRowForDeletion >= 0) {
            model.removeRow(getSelectedRowForDeletion);
            JOptionPane.showMessageDialog(null, "Pesanan dihapus dari keranjang");
        } else {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan.");
        }
        textTotalHarga.setText(totalHarga());
    }
    
    private String totalHarga() {
        String result = "";
        int keranjangRow = tabelKeranjang.getRowCount();

        int temp = 0;
        int valueInt;
        for (int i = 0; i < keranjangRow; i++) {
            String value = tabelKeranjang.getValueAt(i, 2).toString();
            valueInt = Integer.parseInt(value);

            temp += valueInt;
        }

        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();

        formatRp.setCurrencySymbol("Rp. ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');
        kursIndonesia.setDecimalFormatSymbols(formatRp);

        result = kursIndonesia.format(temp);
        return result;
    }

